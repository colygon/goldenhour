  id             uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  name           text        NOT NULL,
  type           text        NOT NULL,
  lat            float8      NOT NULL,
  lon            float8      NOT NULL,
  sunset_rating  int2        NOT NULL,
  sunrise_rating int2        NOT NULL,
  created_at     timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE public.spots ENABLE ROW LEVEL SECURITY;

CREATE POLICY "public_select" ON public.spots
  FOR SELECT USING (true);

